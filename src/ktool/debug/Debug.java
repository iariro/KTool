package ktool.debug;

/**
 * デバッグ用ユーティリティ。
 * @author kumagai
 */
public class Debug
{
	/**
	 * 文字列表示。
	 * @param val 文字列
	 */
	public static void p(String val)
	{
		System.out.println(val);
	}

	/**
	 * boolean値表示。
	 * @param val boolean値
	 */
	public static void p(boolean val)
	{
		System.out.println(val);
	}

	/**
	 * int値表示。
	 * @param val int値
	 */
	public static void p(int val)
	{
		System.out.println(val);
	}

	/**
	 * long値表示。
	 * @param val long値
	 */
	public static void p(long val)
	{
		System.out.println(val);
	}

	/**
	 * Object表示。
	 * @param val Object
	 */
	public static void p(Object val)
	{
		System.out.println(val);
	}
}
